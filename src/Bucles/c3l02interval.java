package Bucles;

import java.util.*;
public class c3l02interval {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        sc.useLocale(Locale.ENGLISH);
        int n = sc.nextInt();
        int m = sc.nextInt();
        for(int i = n; i < m; i++){
            System.out.print(i + "");
        }
    }
}
